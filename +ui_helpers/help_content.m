function entry = help_content(key)
%UI_HELPERS.HELP_CONTENT  Single source of truth for every help string the GUI
%   shows in its ⓘ info buttons + Help menu. Keep all human-readable
%   explanations here — `+app/AorticCenterlineApp.m` should never inline
%   help text directly. That way a documentation pass means editing one
%   file, not 9000 lines of UI code.
%
%   entry = ui_helpers.help_content(KEY)  returns a struct with:
%       .title   short header for the modal (1 line)
%       .body    full explanation, can contain newlines / bullets
%       .when    "When to use this" hint (optional)
%       .auto    "What the Automatic mode does instead" (optional)
%
%   Calling with a missing KEY returns an empty-body entry plus a console
%   warning so a typo doesn't crash the GUI.

%   Project: AINN/EVAR (Phase 3)
%   Author : David P. Stonko

    R = registry();
    if isKey(R, key)
        entry = R(key);
    else
        warning('ui_helpers:help_content:missing', ...
            'No help entry for key "%s" — add one in +ui_helpers/help_content.m', key);
        entry = make_entry(key, '', '', '');
    end
end


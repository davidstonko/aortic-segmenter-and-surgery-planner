        'Auto mode writes all three to the default output directory and prints a summary.');

    R('step6.library') = make_entry( ...
        'step6.library', ...
        'Case library', ...
        [ ...
        'A directory of completed cases used as a benchmark corpus. Each case is a .mat with the full', newline, ...
        'planner state — re-open one to compare against the current case or to bootstrap a similar one.', newline, newline, ...
        'Library cases are also the inputs for the regression test suite, so adding a case can act as', newline, ...
        'an integration test for future code changes.'], '', '');
end

